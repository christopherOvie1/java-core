package calculationPractice;

public class Calc {

	public static void main(String[] args) {
	
		int num1,num2;
		num1=10;
		num2=2;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num1%num2);
		System.out.println(Math.pow(num1, num2));
		System.out.println(Math.pow(num1, num2)+num2);


	}

}
